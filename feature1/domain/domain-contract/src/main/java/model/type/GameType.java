package model.type;

/**
 * Supported Game Types
 */
public enum GameType {
    /**
     * MOBA game like League of Legends
     */
    MOBA,
    /**
     * First Person Shooter (like CS:GO)
     */
    FPS,
    /**
     * Car Race Game
     */
    CAR_RACE,
    /**
     * Card Game (HearthStone)
     */
    CARD_GAME,
    /**
     * Strategy Game (like Starcraft)
     */
    STRATEGY_GAME
}

